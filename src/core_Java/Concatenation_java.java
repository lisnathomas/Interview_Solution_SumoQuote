package core_Java;

public class Concatenation_java {

	public static void main(String[] args) {
		int a = 10;
		int b = 7;
		String x = "Hello";
		String y = "World";
		
		System.out.println(a+b);
		System.out.println(x+y);
		System.out.println(a+b+x+y);
		System.out.println(x+y+a+b);
		System.out.println(x+y+(a+b));
	}

}
